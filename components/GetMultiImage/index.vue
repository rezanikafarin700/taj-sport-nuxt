      >
        <div class="get-multi-image__image--ratio"></div>
        <div class="get-multi-image__overlay">
          <div class="get-multi-image__del-btn" @click="deleteImage(index)">
            <svg
              xmlns="http://www.w3.org/2000/svg"
              width="24"
              height="24"
              viewBox="0 0 24 24"
            >
              <path
                d="M6 7H5v13a2 2 0 0 0 2 2h10a2 2 0 0 0 2-2V7H6zm4 12H8v-9h2v9zm6 0h-2v-9h2v9zm.618-15L15 2H9L7.382 4H3v2h18V4z"
              />
            </svg>
          </div>
        </div>
      </div>

      <div class="get-multi-image__con-input">
        <input
          v-if="!once"
          class="get-multi-image__input"
          type="file"
          :title="titleMulti"
          multiple
          @change="addImages"
        />
        <input
          v-else-if="files.length <= 0"
          class="get-multi-image__input"
          type="file"
          :title="titleOnce"
          @change="addImages"
        />
        <div class="get-multi-image__btn-input">
          <svg
            enable-background="new 0 0 48 48"
            height="100%"
